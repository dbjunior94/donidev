function ImprimirNumero(){
    let i = 1

    while(i <= 50){
        console.log(i)
        i++
    }
}

function contadorDeNumero(){
    for (let i = 1; i <= 50; i++){
        console.log(i)
    }
}
console.log('Exemplo de While')
console.log('=================')
ImprimirNumero()
console.log('=================')
console.log('Exemplo de For')
console.log('=================')
contadorDeNumero()
console.log('=================')