const a = 7
let b = 3

// atribuição de adição
b += a // b = b + a 
console.log(b)

// atribuição de subtração
b -= 4 // b = b - 4
console.log(b)

// atribuição de multiplicação 
b *= 2 // b = b * 2
console.log(b)

// atribuição de divisão
b /= 2 // b = b / 2
console.log(b)

// atribuição de resto
b %= 2 // b = b % 2 
console.log(b)